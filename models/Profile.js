const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const ProfileSchema = Schema({
  user:{
    type: String,
    require: true
  },
  handle:{
    type:String,
    require:true,
    min:2,
    max:30
  },
  status:{
    type:String,
    require:true
  },
  company:{
    type:String
  },
  website:{
    type:String
  },
  location:{
    type:String
  },
  githubUsername:{
    type:String
  },
  skills:{
    type:[String],
    required:true
  },
  bio:{
    type:String,
    min:10,
    max:300
  },
  education:[{
    degree:{
      type:String,
      require:true
    },
    school:{
      type:String,
      require:true
    },
    fieldOfStudy:{
      type:String
    },
    from:{
      type:Date,
      require:true
    },
    to:{
      type:Date,
    },
    current:{
      type:Boolean,
      default:false
    },
    description:{
      type:String,
      min:10,
      max:300
    },
    date:{
      type:Date,
      default:Date.now
    }
  }],
  experience:[{
    title:{
      type:String,
      require:true
    },
    company:{
      type:String
    },
    location:{
      type:String
    },
    from:{
      type:Date,
      require:true
    },
    to:{
      type:Date,
    },
    current:{
      type:Boolean,
      default:false
    },
    description:{
      type:String,
      min:10,
      max:300
    },
    date:{
      type:Date,
      default:Date.now
    }
  }],
  date:{
    type:Date,
    default:Date.now
  },
  // socials
  social:{
    youtube:{
      type:String
    },
    facebook:{
      type:String
    },
    twitter:{
      type:String
    },
    instagram:{
      type:String
    },
    linkedin:{
      type:String
    }
  }
});
module.exports = mongoose.model('Profile', ProfileSchema);